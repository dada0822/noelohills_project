package product;

public class ProductDTO {
	private String p_code;
	private String p_categorycode;
	private String p_name;
	private String p_price;
	private String p_totalprice;
	private String p_count;
	
	public String getP_code() {
		return p_code;
	}
	public void setP_code(String p_code) {
		this.p_code = p_code;
	}
	public String getP_categorycode() {
		return p_categorycode;
	}
	public void setP_categorycode(String p_categorycode) {
		this.p_categorycode = p_categorycode;
	}
	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public String getP_price() {
		return p_price;
	}
	public void setP_price(String p_price) {
		this.p_price = p_price;
	}
	public String getP_totalprice() {
		return p_totalprice;
	}
	public void setP_totalprice(String p_totalprice) {
		this.p_totalprice = p_totalprice;
	}
	public String getP_count() {
		return p_count;
	}
	public void setP_count(String p_count) {
		this.p_count = p_count;
	}
	
}
